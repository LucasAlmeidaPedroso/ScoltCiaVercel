create table if not exists tutors (
  id bigint generated by default as identity primary key,
  full_name text not null,
  phone text,
  whatsapp text,
  email text,
  address text,
  emergency_contact text,
  created_at timestamptz not null default now()
);

create table if not exists app_users (
  id bigint generated by default as identity primary key,
  name text not null,
  email text not null unique,
  role text not null default 'equipe' check (role in ('admin', 'equipe', 'tutor')),
  password_salt text not null,
  password_hash text not null,
  is_active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists pets (
  id bigint generated by default as identity primary key,
  tutor_id bigint references tutors(id) on delete cascade,
  name text not null,
  breed text,
  size text,
  sex text,
  weight numeric,
  birth_date date,
  behavior text,
  food_restrictions text,
  medications text,
  important_notes text,
  veterinarian text,
  photo_url text,
  created_at timestamptz not null default now()
);

create table if not exists reservations (
  id bigint generated by default as identity primary key,
  pet_id bigint references pets(id) on delete set null,
  tutor_name text not null,
  phone text not null,
  email text,
  pet_name text not null,
  breed text,
  size text,
  service text not null,
  entry_date date not null,
  exit_date date,
  expected_time text,
  notes text,
  status text not null default 'Aguardando aprovacao',
  created_at timestamptz not null default now()
);

create table if not exists daycare_settings (
  id bigint primary key default 1,
  max_capacity integer not null default 20,
  updated_at timestamptz not null default now(),
  constraint one_daycare_settings_row check (id = 1)
);

create or replace view pet_options as
select
  pets.id,
  pets.name,
  pets.breed,
  pets.size,
  tutors.full_name as tutor_name,
  tutors.phone as tutor_phone,
  tutors.email as tutor_email,
  pets.birth_date,
  pets.photo_url,
  pets.created_at,
  pets.sex,
  pets.weight,
  pets.behavior,
  pets.food_restrictions,
  pets.medications,
  pets.important_notes,
  pets.veterinarian
from pets
left join tutors on tutors.id = pets.tutor_id;

